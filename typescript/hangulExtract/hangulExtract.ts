type char = string;

function isHangul(word: char) {
  const code = word.charCodeAt(0) - 0xac00;

  // 한글 범위 계산
  const start = "가".charCodeAt(0) - 0xac00;
  const end = "힣".charCodeAt(0) - 0xac00;

  return code >= start && code <= end;
}

